DO $$
BEGIN
    IF EXISTS (
        SELECT 1 FROM information_schema.columns
        WHERE table_schema = 'public' AND table_name = 'category' AND column_name = 'seq'
    ) AND NOT EXISTS (
        SELECT 1 FROM information_schema.columns
        WHERE table_schema = 'public' AND table_name = 'category' AND column_name = 'category_id'
    ) THEN
        ALTER TABLE category RENAME COLUMN seq TO category_id;
        ALTER TABLE category RENAME COLUMN name TO category_name;
    END IF;

    IF EXISTS (
        SELECT 1 FROM information_schema.columns
        WHERE table_schema = 'public' AND table_name = 'gu' AND column_name = 'seq'
    ) AND NOT EXISTS (
        SELECT 1 FROM information_schema.columns
        WHERE table_schema = 'public' AND table_name = 'gu' AND column_name = 'gu_id'
    ) THEN
        ALTER TABLE gu RENAME COLUMN seq TO gu_id;
        ALTER TABLE gu RENAME COLUMN name TO gu_name;
    END IF;

    IF EXISTS (
        SELECT 1 FROM information_schema.columns
        WHERE table_schema = 'public' AND table_name = 'events' AND column_name = 'inquiry_number'
    ) AND NOT EXISTS (
        SELECT 1 FROM information_schema.columns
        WHERE table_schema = 'public' AND table_name = 'events' AND column_name = 'inqury_number'
    ) THEN
        ALTER TABLE events RENAME COLUMN inquiry_number TO inqury_number;
    END IF;
END $$;

