DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM information_schema.columns
        WHERE table_schema = 'public' AND table_name = 'category' AND column_name = 'category_id' AND is_identity = 'YES'
    ) THEN
        ALTER TABLE category ALTER COLUMN category_id ADD GENERATED BY DEFAULT AS IDENTITY;
    END IF;
    IF NOT EXISTS (
        SELECT 1 FROM information_schema.columns
        WHERE table_schema = 'public' AND table_name = 'gu' AND column_name = 'gu_id' AND is_identity = 'YES'
    ) THEN
        ALTER TABLE gu ALTER COLUMN gu_id ADD GENERATED BY DEFAULT AS IDENTITY;
    END IF;
END $$;

SELECT setval(pg_get_serial_sequence('category', 'category_id'), COALESCE((SELECT MAX(category_id) FROM category), 1), true);
SELECT setval(pg_get_serial_sequence('gu', 'gu_id'), COALESCE((SELECT MAX(gu_id) FROM gu), 1), true);

DO $$
BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'events_category_seq_fkey') THEN
        ALTER TABLE events
            ADD CONSTRAINT events_category_seq_fkey
            FOREIGN KEY (category_seq) REFERENCES category(category_id);
    END IF;
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'events_gu_seq_fkey') THEN
        ALTER TABLE events
            ADD CONSTRAINT events_gu_seq_fkey
            FOREIGN KEY (gu_seq) REFERENCES gu(gu_id);
    END IF;
END $$;
